    /// Uses a deterministic UUID derived from a seed string.
    private static func builtInId(_ seed: String) -> String {
        // Use a simple hash-based UUID v5-style ID for stability
        let combined = "com.readassistant.builtin." + seed
        var hash: UInt64 = 0
        for byte in combined.utf8 {
            hash = hash &+ UInt64(byte)
            hash = hash &* 0x01000193
            hash = hash ^ (hash >> 24)
        }
        // Format as UUID-style string
        let part1 = String(format: "%08x", UInt32((hash >> 32) & 0xFFFFFFFF))
        let part2 = String(format: "%04x", UInt16((hash >> 16) & 0xFFFF))
        let part3 = String(format: "%04x", UInt16(hash & 0xFFFF))
        return "\(part1)-\(part2)-\(part3)-0000-000000000000"
    }

